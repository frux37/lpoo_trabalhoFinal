    public void loadTabelaUsuarios() {

        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setNumRows(0);

        for (Usuario u : dao.listaUsuario()) {
            Object[] linha = {
                u,
                u.getNome(),
                u.getEmail(),
                u.getPerfil()
            };

            modelo.addRow(linha);
        }
    }


