    public void loadTabelaInvestimentos() {
    DefaultTableModel modelo = (DefaultTableModel) tblInvestimentos.getModel();
    modelo.setNumRows(0);

    for (Investimento i : dao.listaInvestimentos()) {
        Object[] linha = {
            i,
            i.getNome(),
            i.getTipo(),
            i.getValorInicial(),
            //i.getAporteMensal(),
            i.getRentabilidadeAnual(),
            i.getRisco(),
            i.getDataAplicacao()
        };
        modelo.addRow(linha);
    }
}


