    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        CadastroInvestimentoJD telaCadastro = new CadastroInvestimentoJD(this, rootPaneCheckingEnabled);
        telaCadastro.setVisible(true);

        Investimento novo = telaCadastro.getInvestimento();
        if (novo != null) {
            try {
                dao.persist(novo);
                loadTabelaInvestimentos();
            } catch (Exception ex) {
                System.err.println("Erro ao adicionar um investimento: " + ex);
            }

        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        if (tblInvestimentos.getSelectedRow() != -1) {
            Investimento obj = (Investimento) tblInvestimentos.getModel().
                    getValueAt(tblInvestimentos.getSelectedRow(), 0);
            int op_remover = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que deseja remover " + obj + "?");
            if (op_remover == JOptionPane.YES_OPTION) {
                try {
                    dao.remover(obj);
                    JOptionPane.showMessageDialog(rootPane, "Usuario removido com sucesso... ");
                    loadTabelaInvestimentos();
                } catch (Exception ex) {
                    System.err.println("Erro ao remover usuario: " + ex);
                }
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um cliente");
        }
        
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (tblInvestimentos.getSelectedRow() != -1) {
            Investimento obj = (Investimento) tblInvestimentos.getModel().
                    getValueAt(tblInvestimentos.getSelectedRow(), 0);

            CadastroInvestimentoJD telaEdicao = new CadastroInvestimentoJD(this, rootPaneCheckingEnabled);
            telaEdicao.setInvestimento(obj);

            telaEdicao.setVisible(true);

            Investimento obj_retornado = telaEdicao.getInvestimento();

            if (obj_retornado != null) {

                try {
                    dao.persist(obj_retornado);
                    loadTabelaInvestimentos();
                } catch (Exception ex) {
                    System.err.println("Erro ao editar Usuario: " + ex);
                }

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um Usuario");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

